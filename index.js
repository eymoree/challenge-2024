  function copiarTexto() {
    var textarea = document.getElementById("texto");
    
    navigator.clipboard.writeText(textarea.value)
      .then(() => {
        alert("Texto copiado: " + textarea.value);
      })
      .catch(err => {
        console.error("Error al copiar el texto: ", err);
      });
  }
  
